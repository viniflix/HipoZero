-- ─── 3) conduct_suggestions (conduta por exames + objetivo) ────
create table if not exists public.conduct_suggestions (
    id              bigint generated by default as identity primary key,
    nutritionist_id uuid not null references public.user_profiles(id) on delete cascade,
    patient_id      uuid not null references public.user_profiles(id) on delete cascade,
    suggestion_key  text not null,
    title           text not null,
    rationale       text not null,
    suggested_conduct text null,
    lab_context     jsonb not null default '[]'::jsonb,
    goal_context    jsonb null,
    status          text not null default 'pending'
        check (status in ('pending','approved','rejected')),
    approved_by     uuid null references public.user_profiles(id) on delete set null,
    approved_at     timestamptz null,
    rejected_by     uuid null references public.user_profiles(id) on delete set null,
    rejected_at     timestamptz null,
    rejection_reason text null,
    metadata        jsonb not null default '{}'::jsonb,
    created_at      timestamptz not null default now(),
    updated_at      timestamptz not null default now()
);

create index if not exists conduct_suggestions_nutritionist_status_idx
    on public.conduct_suggestions (nutritionist_id, status, created_at desc);
create index if not exists conduct_suggestions_patient_idx
    on public.conduct_suggestions (patient_id, created_at desc);

create or replace function public.set_conduct_suggestions_updated_at()
returns trigger language plpgsql as $$
begin
    new.updated_at = clock_timestamp();
    return new;
end;
$$;
drop trigger if exists trg_set_conduct_suggestions_updated_at on public.conduct_suggestions;
