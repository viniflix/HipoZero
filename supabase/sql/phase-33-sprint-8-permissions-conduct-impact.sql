-- ─── 4) RPC evaluate_lab_goal_rules ─────────────────────────────
-- Avalia exames + meta do paciente e retorna sugestões de conduta.
create or replace function public.evaluate_lab_goal_rules(
    p_nutritionist_id uuid,
    p_patient_id      uuid
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    v_actor         uuid := auth.uid();
    v_labs          jsonb;
    v_goal          jsonb;
    v_suggestions   jsonb := '[]'::jsonb;
    v_sug           jsonb;
    v_lab           record;
    v_normalized    text;
    v_lab_array     jsonb;
begin
    if v_actor is null then
        return jsonb_build_object('ok', false, 'reason', 'unauthenticated');
    end if;

    -- Buscar exames recentes anormais (usa cursor direto)
    v_labs := coalesce((
        select jsonb_agg(jsonb_build_object(
            'id', lr.id, 'test_name', lr.test_name, 'marker_key', lr.marker_key,
            'test_value', lr.test_value, 'status', lr.status, 'test_date', lr.test_date
        ))
        from public.lab_results lr
        where lr.patient_id = p_patient_id
          and lr.status in ('high','low')
        order by lr.test_date desc nulls last
        limit 20
    ), '[]'::jsonb);

    -- Buscar meta ativa
    select to_jsonb(pg.*) into v_goal
    from public.patient_goals pg
    where pg.patient_id = p_patient_id
      and pg.status = 'active'
    order by pg.created_at desc
    limit 1;

    -- Gerar sugestões por exame anormal
    for v_lab in
        select lr.id, lr.test_name, lr.marker_key, lr.test_value, lr.status, lr.test_date
        from public.lab_results lr
        where lr.patient_id = p_patient_id and lr.status in ('high','low')
        order by lr.test_date desc nulls last
        limit 20
    loop
        v_normalized := lower(replace(coalesce(nullif(trim(v_lab.marker_key), ''), v_lab.test_name, 'exame'), ' ', '_'));
        v_normalized := regexp_replace(v_normalized, '[^a-z0-9_]', '', 'g');
        if length(v_normalized) = 0 then v_normalized := 'exame'; end if;

        v_lab_array := jsonb_build_array(jsonb_build_object(
            'id', v_lab.id, 'test_name', v_lab.test_name, 'test_value', v_lab.test_value,
            'status', v_lab.status, 'test_date', v_lab.test_date
        ));

        if v_lab.status = 'high' then
            v_sug := jsonb_build_object(
                'suggestion_key', 'lab_high_' || v_normalized,
                'title', 'Exame acima da referência: ' || coalesce(v_lab.test_name, 'Exame'),
                'rationale', format('Valor %s acima do esperado. Ajustar conduta nutricional conforme protocolo.', v_lab.test_value),
                'suggested_conduct', 'Revisar plano alimentar e suplementação. Considerar retorno em 4-6 semanas.',
                'lab_context', v_lab_array,
                'goal_context', v_goal
            );
        else
            v_sug := jsonb_build_object(
                'suggestion_key', 'lab_low_' || v_normalized,
                'title', 'Exame abaixo da referência: ' || coalesce(v_lab.test_name, 'Exame'),
                'rationale', format('Valor %s abaixo do esperado. Avaliar ingestão e absorção.', v_lab.test_value),
                'suggested_conduct', 'Avaliar ingestão alimentar. Considerar suplementação se necessário.',
                'lab_context', v_lab_array,
                'goal_context', v_goal
            );
        end if;
        v_suggestions := v_suggestions || jsonb_build_array(v_sug);
    end loop;

    -- Sugestão combinada se tem meta + exames alterados
    if v_goal is not null and jsonb_array_length(v_labs) > 0 then
        v_sug := jsonb_build_object(
            'suggestion_key', 'goal_lab_combined',
            'title', 'Conduta: meta de peso + exames alterados',
            'rationale', 'Paciente com meta ativa e exames fora da faixa. Priorizar normalização dos marcadores.',
            'suggested_conduct', 'Ajustar déficit calórico de forma gradual. Monitorar exames em 4 semanas.',
            'lab_context', v_labs,
            'goal_context', v_goal
        );
        v_suggestions := v_suggestions || jsonb_build_array(v_sug);
    end if;

    return jsonb_build_object(
        'ok', true,
        'suggestions', v_suggestions,
        'lab_count', jsonb_array_length(v_labs),
        'has_goal', v_goal is not null
    );
end;
$$;

grant execute on function public.evaluate_lab_goal_rules(uuid, uuid) to authenticated;


-- ─── 5) RPC approve/reject conduct ─────────────────────────────
create or replace function public.approve_conduct_suggestion(
    p_suggestion_id bigint,
    p_actor_id      uuid default auth.uid()
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    v_row record;
begin
    if auth.uid() is null then
        return jsonb_build_object('ok', false, 'reason', 'unauthenticated');
    end if;

    select * into v_row from public.conduct_suggestions
    where id = p_suggestion_id and nutritionist_id = auth.uid();

    if not found then
        return jsonb_build_object('ok', false, 'reason', 'not_found');
    end if;

    if v_row.status != 'pending' then
        return jsonb_build_object('ok', false, 'reason', 'already_processed');
    end if;

    update public.conduct_suggestions
    set status = 'approved',
        approved_by = p_actor_id,
        approved_at = clock_timestamp(),
        rejected_by = null,
        rejected_at = null,
        rejection_reason = null
    where id = p_suggestion_id;

    return jsonb_build_object('ok', true, 'status', 'approved');
end;
$$;

create or replace function public.reject_conduct_suggestion(
    p_suggestion_id bigint,
    p_reason        text default null,
    p_actor_id      uuid default auth.uid()
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    v_row record;
begin
    if auth.uid() is null then
        return jsonb_build_object('ok', false, 'reason', 'unauthenticated');
    end if;

    select * into v_row from public.conduct_suggestions
    where id = p_suggestion_id and nutritionist_id = auth.uid();

    if not found then
        return jsonb_build_object('ok', false, 'reason', 'not_found');
    end if;

    if v_row.status != 'pending' then
        return jsonb_build_object('ok', false, 'reason', 'already_processed');
    end if;

    update public.conduct_suggestions
    set status = 'rejected',
        rejected_by = p_actor_id,
        rejected_at = clock_timestamp(),
        rejection_reason = p_reason,
        approved_by = null,
        approved_at = null
    where id = p_suggestion_id;

    return jsonb_build_object('ok', true, 'status', 'rejected');
end;
$$;

grant execute on function public.approve_conduct_suggestion(bigint, uuid) to authenticated;
grant execute on function public.reject_conduct_suggestion(bigint, text, uuid) to authenticated;
